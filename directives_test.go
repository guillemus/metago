// Every //mgo: comment must be a well-formed directive; typos error instead of silently no-oping.
func TestInvalidDirectivesError(t *testing.T) {
	template := `{{ define "stringer" }}
func (x {{ name . }}) String() string { return string(x) }
{{ end }}
`
	cases := map[string]struct {
		model string
		want  string
	}{
		"unknown verb": {
			model: "package fixture\n\n//mgo:stringer Status\ntype Status string\n",
			want:  `invalid //mgo: directive "//mgo:stringer Status"`,
		},
		"empty directive": {
			model: "package fixture\n\n//mgo:\ntype Status string\n",
			want:  `invalid //mgo: directive "//mgo:"`,
		},
		"gen without template name": {
			model: "package fixture\n\n//mgo:gen\ntype Status string\n",
			want:  "missing a template name",
		},
		"inline without template name": {
			model: "package fixture\n\n//mgo:inline\ntype Status string\n",
			want:  "missing a template name",
		},
	}
	for name, tc := range cases {
		t.Run(name, func(t *testing.T) {
			dir := t.TempDir()
			writeTestFile(t, filepath.Join(dir, "model.go"), tc.model)
			writeTestFile(t, filepath.Join(dir, "templates.metago"), template)

			_, err := generate(dir)
			if err == nil {
				t.Fatal("expected invalid directive error")
			}
			if !strings.Contains(err.Error(), tc.want) || !strings.Contains(err.Error(), "model.go:3") {
				t.Fatalf("expected %q and model.go:3 in error, got: %v", tc.want, err)
			}
		})
	}
}

// //mgo:end alone is a marker, not an annotation, and never errors.
func TestEndDirectiveAloneIsIgnored(t *testing.T) {
	dir := t.TempDir()
	writeTestFile(t, filepath.Join(dir, "model.go"), "package fixture\n\n//mgo:end\ntype Status string\n")
	writeTestFile(t, filepath.Join(dir, "templates.metago"), `{{ define "stringer" }}x{{ end }}`)

	got, err := generate(dir)
	if err != nil {
		t.Fatal(err)
	}
	if len(got) != 0 {
		t.Fatalf("stray //mgo:end should generate nothing, got:\n%s", got)
	}
}

// An //mgo:end with trailing whitespace still terminates its inline block; otherwise regeneration
// would insert a duplicate block above the stale marker.
func TestEndDirectiveToleratesTrailingWhitespace(t *testing.T) {
	dir := t.TempDir()
	model := filepath.Join(dir, "model.go")
	writeTestFile(t, model, "package fixture\n\ntype A string\n\n//mgo:inline stringer A\n\nfunc (a A) Old() string { return \"stale\" }\n\n//mgo:end \n")
	writeTestFile(t, filepath.Join(dir, "templates.metago"), `{{ define "stringer" }}
func (a {{ name . }}) String() string { return string(a) }
{{ end }}
`)

	files, err := generateFiles(dir)
	if err != nil {
		t.Fatal(err)
	}
	got := string(files[model])
	if strings.Contains(got, "Old()") {
		t.Fatalf("stale block should be replaced despite trailing space on //mgo:end, got:\n%s", got)
	}
	if strings.Count(got, "String()") != 1 {
		t.Fatalf("generated block should appear exactly once, got:\n%s", got)
	}
}

