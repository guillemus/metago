// An //mgo:end with trailing whitespace still terminates its inline block; otherwise regeneration
// would insert a duplicate block above the stale marker.
func TestEndDirectiveToleratesTrailingWhitespace(t *testing.T) {
	dir := t.TempDir()
	model := filepath.Join(dir, "model.go")
	writeTestFile(t, model, "package fixture\n\ntype A string\n\n//mgo:inline stringer A\n\nfunc (a A) Old() string { return \"stale\" }\n\n//mgo:end \n")
	writeTestFile(t, filepath.Join(dir, "templates.metago"), `{{ define "stringer" }}
func (a {{ name . }}) String() string { return string(a) }
{{ end }}
`)

	files, err := generateFiles(dir)
	if err != nil {
		t.Fatal(err)
	}
	got := string(files[model])
	if strings.Contains(got, "Old()") {
		t.Fatalf("stale block should be replaced despite trailing space on //mgo:end, got:\n%s", got)
	}
	if strings.Count(got, "String()") != 1 {
		t.Fatalf("generated block should appear exactly once, got:\n%s", got)
	}
}

