// Props attach to the nearest symbol in the same file: types, fields (doc and trailing position),
// methods, functions, interface methods, and embedded fields.
func TestPropsAttachToSymbols(t *testing.T) {
	dir := t.TempDir()
	writeTestFile(t, filepath.Join(dir, "model.go"), `package fixture

//mgo:gen summary User

//mgo:props api owner=core
type User struct {
	//mgo:props validate required max=2000
	Text string

	Rating int //mgo:props validate min=1 max=5

	//mgo:props flatten
	Base
}

type Base struct{}

//mgo:props route member
func (u User) Show() {}

//mgo:props job cron=daily
func Cleanup() {}

type Store interface {
	//mgo:props scope admin
	Delete(id string) error
}
`)
	writeTestFile(t, filepath.Join(dir, "templates.metago"), `{{ define "summary" }}
{{- $store := index .Package.Types 2 -}}
{{- $cleanup := index .Package.Functions 0 -}}
const Summary = {{ quote (printf "type=%s text_req=%t text_max=%s rating_min=%s rating_max=%s base=%t method=%t fn=%s iface=%t"
	(prop . "api" "owner")
	(propHas (index .Fields 0) "validate" "required")
	(prop (index .Fields 0) "validate" "max")
	(prop (index .Fields 1) "validate" "min")
	(prop (index .Fields 1) "validate" "max")
	(propExists (index .Fields 2) "flatten")
	(propHas (index .Methods 0) "route" "member")
	(prop $cleanup "job" "cron")
	(propHas (index $store.Methods 0) "scope" "admin")) }}
{{ end }}
`)

	got, err := generate(dir)
	if err != nil {
		t.Fatal(err)
	}
	want := "// Code generated by metago; DO NOT EDIT.\n\npackage fixture\n\nconst Summary = \"type=core text_req=true text_max=2000 rating_min=1 rating_max=5 base=true method=true fn=daily iface=true\"\n"
	if string(got) != want {
		t.Fatalf("generated output mismatch\n\n--- got ---\n%s\n--- want ---\n%s", got, want)
	}
}

