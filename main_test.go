func TestTestMetasUseSeparatePackageSidecars(t *testing.T) {
	dir := t.TempDir()
	writeTestFile(t, filepath.Join(dir, "model.go"), `package store

type Repository interface { Get() string }
`)
	writeTestFile(t, filepath.Join(dir, "internal_test.go"), `package store

//mgo:gen summary InternalFixture
type InternalFixture struct{}
`)
	writeTestFile(t, filepath.Join(dir, "external_test.go"), `package store_test

//mgo:gen summary ExternalFixture
type ExternalFixture struct{}
`)
	writeTestFile(t, filepath.Join(dir, "templates.metago"), `{{ define "summary" }}
const Generated{{ name . }} = true
{{ end }}
`)

	files, err := generateFiles(dir)
	if err != nil {
		t.Fatal(err)
	}
	internal := string(files[filepath.Join(dir, "meta_test.go")])
	if !strings.Contains(internal, "package store") || !strings.Contains(internal, "GeneratedInternalFixture") {
		t.Fatalf("unexpected internal test sidecar:\n%s", internal)
	}
	external := string(files[filepath.Join(dir, "meta_store_test.go")])
	if !strings.Contains(external, "package store_test") || !strings.Contains(external, "GeneratedExternalFixture") {
		t.Fatalf("unexpected external test sidecar:\n%s", external)
	}
	if _, ok := files[filepath.Join(dir, "meta.go")]; ok {
		t.Fatal("test directives must not generate production meta.go")
	}

	// Exercise the complete write path twice: generated sidecars must be ignored on the second scan.
	if err := run(dir); err != nil {
		t.Fatal(err)
	}
	if err := run(dir); err != nil {
		t.Fatalf("regeneration scanned its own output: %v", err)
	}
	writeTestFile(t, filepath.Join(dir, "go.mod"), "module example.com/store\n\ngo 1.23\n")
	cmd := exec.Command("go", "test", "./...")
	cmd.Dir = dir
	if output, err := cmd.CombinedOutput(); err != nil {
		t.Fatalf("generated internal and external test packages do not compile: %v\n%s", err, output)
	}
}

