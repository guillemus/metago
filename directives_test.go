// prop helpers are nil-safe on symbols without any props.
func TestPropHelpersNilSafety(t *testing.T) {
	field := Field{Name: "Plain"}
	if propValue(field, "g", "k") != "" {
		t.Fatal("prop on propless field should be empty")
	}
	if propHasFlag(field, "g", "flag") {
		t.Fatal("propHas on propless field should be false")
	}
	if propGroupExists(field, "g") {
		t.Fatal("propExists on propless field should be false")
	}
	if group := propGroup(field, "g"); group.Group != "" || len(group.Args) != 0 || len(group.Argv) != 0 {
		t.Fatalf("props on propless field should be zero, got %#v", group)
	}
	if propsOf(nil) != nil {
		t.Fatal("propsOf(nil) should be nil")
	}
	if propValue("not a symbol", "g", "k") != "" {
		t.Fatal("prop on non-symbol should be empty")
	}
}

