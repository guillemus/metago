// One aggregator annotation can read every annotation in the package via .Package.Metas, in
// deterministic file-then-line order, with args, argv, and location. Props metas attach to
// symbols instead and never appear there. This is the primitive for many-annotations-one-artifact
// generators such as HTTP route tables.
func TestPackageMetasAggregation(t *testing.T) {
	dir := t.TempDir()
	writeTestFile(t, filepath.Join(dir, "a.go"), `package fixture

//mgo:gen get /posts/{postID} auth=required
func ShowPost() {}

//mgo:props internal flag
type helper struct{}
`)
	writeTestFile(t, filepath.Join(dir, "b.go"), `package fixture

//mgo:gen post /posts
func CreatePost() {}

//mgo:gen server
type Server struct{}
`)
	writeTestFile(t, filepath.Join(dir, "templates.metago"), `{{ define "get" }}{{ end }}
{{ define "post" }}{{ end }}
{{ define "server" }}
const Routes = {{ quote (printf "%s" .Package.Metas) }}
const Table = "
{{- range .Package.Metas }}
{{- if or (eq .Template "get") (eq .Template "post") }}
{{- upper .Template }} {{ index .Argv 0 }} auth={{ index .Args "auth" }};
{{- end }}
{{- end }}"
{{ end }}
`)

	got, err := generate(dir)
	if err != nil {
		t.Fatal(err)
	}
	output := string(got)
	if !strings.Contains(output, `const Table = "GET /posts/{postID} auth=required;POST /posts auth=;"`) {
		t.Fatalf("aggregated route table mismatch, got:\n%s", output)
	}
	if strings.Contains(output, "props") || strings.Contains(output, "internal") {
		t.Fatalf("props metas must not appear in .Package.Metas, got:\n%s", output)
	}
}

// The aggregator sees inline annotations too, marked with Inline.
func TestPackageMetasIncludeInlineAnnotations(t *testing.T) {
	dir := t.TempDir()
	writeTestFile(t, filepath.Join(dir, "model.go"), `package fixture

type A string

//mgo:inline stringer A

//mgo:gen summary A
`)
	writeTestFile(t, filepath.Join(dir, "templates.metago"), `{{ define "stringer" }}
func (a {{ name . }}) String() string { return string(a) }
{{ end }}
{{ define "summary" }}
const Summary = {{ quote (printf "%d" (len .Package.Metas)) }}
{{- range .Package.Metas }}{{ if .Inline }}
const InlineTemplate = {{ quote .Template }}
{{- end }}{{ end }}
{{ end }}
`)

	files, err := generateFiles(dir)
	if err != nil {
		t.Fatal(err)
	}
	sidecar := string(files[filepath.Join(dir, "meta.go")])
	if !strings.Contains(sidecar, `const Summary = "2"`) || !strings.Contains(sidecar, `const InlineTemplate = "stringer"`) {
		t.Fatalf("expected inline meta in .Package.Metas, got:\n%s", sidecar)
	}
}
